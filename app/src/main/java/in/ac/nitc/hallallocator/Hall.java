package in.ac.nitc.hallallocator;

public class Hall {
    Integer hall_capacity;
    String hall_incharge;
    String hall_name;

    public Integer getHall_capacity() {
        return hall_capacity;
    }

    public void setHall_capacity(Integer hall_capacity) {
        this.hall_capacity = hall_capacity;
    }

    public String getHall_incharge() {
        return hall_incharge;
    }

    public void setHall_incharge(String hall_incharge) {
        this.hall_incharge = hall_incharge;
    }

    public String getHall_name() {
        return hall_name;
    }

    public void setHall_name(String hall_name) {
        this.hall_name = hall_name;
    }
}


